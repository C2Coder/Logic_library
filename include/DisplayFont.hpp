static char utf8toCp(const char* utf8) {
    auto us = (const uint8_t*)utf8;
    const int len = strlen(utf8);
    if (len == 0) {
        return 0;
    } else if (len == 1 || us[0] < 128) {
        return utf8[0];
    }

    switch (us[1] << 8 | us[0]) {
    case 0xa1c3: // á
        return 0xE1;
    case 0x8dc4: // č
        return 'c';
    case 0x8fc4: // ď
        return 'd';
    case 0xa9c3: // é
        return 0xE9;
    case 0x9bc4: // ě
        return 0xEA;
    case 0xadc3: // í
        return 0xED;
    case 0x88c5: // ň
        return 'n';
    case 0xb3c3: // ó
        return 0xF3;
    case 0x99c5: // ř
        return 'r';
    case 0xa1c5: // š
        return 's';
    case 0xa5c5: // ť
        return 't';
    case 0xbac3: // ú
        return 0xFA;
    case 0xafc5: // ů
        return 0xFC;
    case 0xbdc3: // ý
        return 0xFD;
    case 0xbec5: // ž
        return 'z';
    case 0x81c3: // Á
        return 0xC1;
    case 0x8cc4: // Č
        return 'C';
    case 0x8ec4: // Ď
        return 'D';
    case 0x89c3: // É
        return 0xC9;
    case 0x9ac4: // Ě
        return 0xCA;
    case 0x8dc3: // Í
        return 0xCD;
    case 0x87c5: // Ň
        return 'N';
    case 0x93c3: // Ó
        return 0xD3;
    case 0x98c5: // Ř
        return 'R';
    case 0xa0c5: // Š
        return 'S';
    case 0xa4c5: // Ť
        return 'T';
    case 0x9ac3: // Ú
        return 0xDA;
    case 0xaec5: // Ů
        return 0xDC;
    case 0x9dc3: // Ý
        return 0xDD;
    case 0xbdc5: // Ž
        return 'Z';
    default:
        return 0;
    }
}
